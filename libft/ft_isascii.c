#include "libft.h"

int	ft_isascii(char *str)
{
	int	i;

	if (*str == '\0')
	{
		return (1);
	}
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0 && str[i] < 127)
			i++;
		else
			return (0);
	}
	return (1);
}
